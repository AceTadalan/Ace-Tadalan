#include <iostream>
using namespace std; 

int main () {
	int x=4, y=2; 
	
	
	
	int sum= x+y;
	cout << "Sum:" << sum << endl;
	
	int difference= x-y; 
	cout << "Difference:" << difference << endl;
	
	int product= x*y ;
	cout << "Product:" << product << endl;
	
	int quotient= x/y ;
	cout << "Quotient:" << quotient << endl;
	
	int remainder= x%y;
	cout << "Remainder:" << remainder << endl;
	
	return 0;
	 
}
